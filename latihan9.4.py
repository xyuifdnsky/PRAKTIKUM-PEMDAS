#Proses memasukkan variabel
rangga_jari_jari = float(input("Masukkan jari-jari lingkaran: "))

#konstanta dari pi
rangga_pi = 3.14159

#Menghitung luas lingkaran
rangga_luas = rangga_pi * rangga_jari_jari ** 2

#Membuat keterangan tampilan output
print("Luas lingkaran dengan jari-jari", rangga_jari_jari, "adalah", rangga_luas)


